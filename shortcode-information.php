    <h1>List of shortcodes</h1><br>
    <p style="font-size:17px;"><b>1.</b> Shortcode to list all the services.</p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services]</code></p><br>

    <p style="font-size:17px;"><b>2.</b> Shortcode to list all the services related to an category. just put the category attribute to shortcode with the id of the caregory.</p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services category="1"]</code></p><br>
    <p style="font-size:17px;">Change the category value <code>1</code> with the category id you wants to show. you can also add comma separated list of categories id like <code>1,2,4,6</code></p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services category="1,2,4,6"]</code></p><br>

    <p style="font-size:17px;"><b>3.</b> This will allow you to limit the number of services you want to show.</p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services numberofpost="1"]</code></p><br>
    <p style="font-size:17px;">Change the numberofpost value <code>1</code> with the number of post you wants to show.</p><br>

    <p style="font-size:17px;"><b>4.</b> This arrttibute will allow you to chnage the layout of the services diaplay. just put the displaytype attribute to shortcode with the value either <code> list</code> or  <code>grid</code>.</p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services displaytype="list"]</code></p><br>
    <p style="font-size:17px;"><b>or</b></p><br>
    <p style="font-size:17px;"><code style="padding:10px;">[services displaytype="grid"]</code></p><br>
</div>